package org.example.enums;

public enum Plan {
    BASIC("Basic Plan", 10),
    INTERMEDIATE("Intermediate Plan", 25),
    PRO("Pro Plan", 50);

    private String name;
    private int price;

    Plan(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
